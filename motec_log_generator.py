    def _on_close(self):
        if self.closed:
            return
        self.closed = True

        if self.layout_refresh_job is not None:
            try:
                self.root.after_cancel(self.layout_refresh_job)
            except tk.TclError:
                pass
            self.layout_refresh_job = None

        if self.ui_queue_after_id is not None:
            try:
                self.root.after_cancel(self.ui_queue_after_id)
            except tk.TclError:
                pass
            self.ui_queue_after_id = None

        if self.active_executor is not None:
            try:
                self.active_executor.shutdown(wait=False, cancel_futures=True)
            except Exception:
                pass
            self.active_executor = None

        if self.preview_executor is not None:
            try:
                self.preview_executor.shutdown(wait=False, cancel_futures=True)
            except Exception:
                pass
            self.preview_executor = None

        try:
            if self.preview_canvas_widget is not None:
                self.preview_canvas_widget.destroy()
        except Exception:
            pass

        self._cancel_segment_cell_edit()

        try:
            if self.preview_figure is not None:
                self.preview_figure.clear()
        except Exception:
            pass

        try:
            self.root.unbind_all("<MouseWheel>")
            self.root.unbind_all("<Shift-MouseWheel>")
            self.root.unbind_all("<Button-4>")
            self.root.unbind_all("<Button-5>")
            self.root.unbind_all("<Shift-Button-4>")
            self.root.unbind_all("<Shift-Button-5>")
        except tk.TclError:
            pass

        try:
            self.root.withdraw()
            self.root.quit()
            self.root.destroy()
        except tk.TclError:
            pass


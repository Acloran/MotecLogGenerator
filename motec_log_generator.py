    def _create_scrollable_panel(self, parent, background, frame_style, padding):
        outer = tk.Frame(parent, bg=background, highlightthickness=0, bd=0)
        canvas = tk.Canvas(
            outer,
            bg=background,
            bd=0,
            highlightthickness=0,
            relief=tk.FLAT,
        )
        scrollbar = AutoHideScrollbar(
            outer,
            orient=tk.VERTICAL,
            command=canvas.yview,
            style="Overlay.Vertical.TScrollbar",
        )
        canvas.configure(yscrollcommand=scrollbar.set)
        canvas.pack(side=tk.LEFT, fill=tk.BOTH, expand=True)
        scrollbar.pack(side=tk.RIGHT, fill=tk.Y)

        inner = ttk.Frame(canvas, style=frame_style, padding=padding)
        window_id = canvas.create_window((0, 0), window=inner, anchor=tk.NW)

        inner.bind(
            "<Configure>",
            lambda _event, current_canvas=canvas: current_canvas.configure(
                scrollregion=current_canvas.bbox("all")
            ),
        )
        canvas.bind(
            "<Configure>",
            lambda event, current_canvas=canvas, current_window=window_id: current_canvas.itemconfigure(
                current_window,
                width=event.width,
            ),
        )
        self._register_scroll_target(canvas, canvas.yview_scroll)
        self._register_scroll_target(inner, canvas.yview_scroll)
        return outer, inner, canvas


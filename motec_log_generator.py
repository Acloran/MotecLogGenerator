    def _remove_selected_segment(self):
        selected_items = self._selected_items()
        if len(selected_items) != 1:
            return
        item = selected_items[0]
        selected_segment_ids = self.segment_tree.selection()
        if not selected_segment_ids:
            if self.pending_segment_active:
                self._clear_pending_segment()
                self._populate_segment_table(item.settings.segment_ranges)
                self._show_single_item_preview(item)
            return

        selected_row_id = selected_segment_ids[0]
        if selected_row_id == "seg-new":
            self._clear_pending_segment()
            self._populate_segment_table(item.settings.segment_ranges)
            self._show_single_item_preview(item)
            return

        segment_index = max(0, int(selected_row_id.split("-")[-1]) - 1)
        if segment_index >= len(item.settings.segment_ranges):
            return

        self._push_undo_state()
        item.settings.segment_ranges.pop(segment_index)
        self._populate_metadata_editor([item])
        self._refresh_tree_row(item.item_id)
        self._show_single_item_preview(item)
        self.summary_var.set("Removed one planned segment.")

    def _apply_segment_plan_to_queue(self):
        selected_items = self._selected_items()
        if len(selected_items) != 1:
            return

        item = selected_items[0]
        if item.preview_log is None:
            return

        segment_ranges = normalize_segment_ranges(
            item.settings.segment_ranges or [(item.preview_log.start(), item.preview_log.end())],
            item.preview_log.start(),
            item.preview_log.end(),
        )
        if not segment_ranges:
            self.summary_var.set("There are no valid segments to create.")
            return

        self._push_undo_state()
        insertion_index = self.files_tree.index(item.item_id)
        created_item_ids = []
        total_segments = len(segment_ranges)

        for segment_number, (start_time, end_time) in enumerate(segment_ranges, start=1):
            segment_log = item.preview_log.extract_segment(start_time, end_time, rebase_time=True)
            segment_settings = item.settings.copy()
            segment_settings.segment_ranges = [(segment_log.start(), segment_log.end())]

            output_stem = "%s_part%d" % ((item.output_stem or Path(item.path).stem), segment_number)
            display_base = Path(item.name).stem if "." in item.name else item.name
            display_name = "%s %d" % (display_base, segment_number) if total_segments > 1 else "%s Trim" % display_base
            new_item = FileItem(
                item_id=self._new_item_id(),
                path=item.path,
                detected_type=item.detected_type,
                status="Preview Ready",
                duration=segment_log.duration(),
                preview_log=segment_log,
                preview_channels=segment_log.channel_names(),
                settings=segment_settings,
                display_name=display_name,
                output_stem=output_stem,
                derived_from=item.path,
            )
            self.file_items[new_item.item_id] = new_item
            self.files_tree.insert(
                "",
                insertion_index + len(created_item_ids),
                iid=new_item.item_id,
                text=new_item.name,
                values=(
                    new_item.detected_type,
                    new_item.duration_text,
                    new_item.size_text,
                ),
            )
            created_item_ids.append(new_item.item_id)

        if item.item_id in self.file_items:
            self.files_tree.delete(item.item_id)
            self.file_items.pop(item.item_id, None)

        self._update_file_column_width()
        self.files_tree.selection_set(created_item_ids)
        if created_item_ids:
            self.files_tree.focus(created_item_ids[0])
            self.files_tree.see(created_item_ids[0])
        self._on_tree_selection_changed()
        self.summary_var.set("Created %d queue item(s) from %s." % (len(created_item_ids), item.name))

    def _set_running_state(self, running):
        self.generation_running = running
        state = tk.DISABLED if running else tk.NORMAL
        combo_state = "disabled" if running else "readonly"

        for widget in (
            self.add_files_btn,
            self.undo_btn,
            self.generate_btn,
            self.dbc_button,
            self.unit_chart_button,
            self.apply_metadata_btn,
            *self.segment_action_buttons,
        ):
            widget.config(state=state)

        for entry in (
            self.freq_entry,
            self.output_entry,
            self.dbc_entry,
            self.unit_chart_entry,
        ):
            entry.config(state=state)

        self.filter_combo.config(state=combo_state)
        if running:
            for entry in self.metadata_entries:
                entry.config(state="disabled")
            self.preview_channel_combo.config(state="disabled")
            self.motion_channel_combo.config(state="disabled")
            self.segment_tree.config(selectmode="none")
            return

        selected_count = len(self._selected_items())
        self._set_editor_enabled(selected_count > 0, single_preview=(selected_count == 1))
        self._update_undo_button_state()

    def _validate_before_generate(self):
        if not self.file_items:
            messagebox.showerror("No Files", "Add at least one file before generating output.")
            return False

        try:
            resolve_frequency(self.freq_var.get())
        except ValueError as exc:
            messagebox.showerror("Invalid Frequency", str(exc))
            return False

        can_items = [item for item in self.file_items.values() if item.detected_type == "CAN"]
        if can_items and not self.dbc_path.get().strip():
            messagebox.showerror("DBC Required", "A DBC file is required when CAN logs are in the list.")
            return False

        try:
            load_channel_unit_chart(self.unit_chart_path.get().strip())
        except Exception as exc:
            messagebox.showerror("Invalid Channel Chart", str(exc))
            return False

        self._apply_metadata_to_selected()
        return True

    def _generate(self):
        if self.generation_running or not self._validate_before_generate():
            return

        self._set_running_state(True)
        self.progressbar["value"] = 0
        self.status_var.set("Preparing queue...")
        self.summary_var.set("Building the conversion queue.")

        items = list(self.file_items.values())
        output_dir = self.output_dir.get().strip()
        frequency_text = self.freq_var.get()
        dbc_path = self.dbc_path.get().strip()
        unit_chart_path = self.unit_chart_path.get().strip()
        threading.Thread(
            target=self._generate_thread,
            args=(items, output_dir, frequency_text, dbc_path, unit_chart_path),
            daemon=True,
        ).start()

    def _generate_thread(self, items, output_dir, frequency_text, dbc_path, unit_chart_path):
        if self.closed:
            return
        can_db = None
        try:
            channel_unit_chart = load_channel_unit_chart(unit_chart_path)
            if any(item.detected_type == "CAN" for item in items):
                can_db = load_can_database(dbc_path)
        except Exception as exc:
            self._enqueue_ui(lambda: self._finish_generation_with_error(str(exc)))
            return

        total = len(items)
        workers = min(max(1, os.cpu_count() or 1), total, 4)

        completed = 0
        self._enqueue_ui(lambda: self.status_var.set("Converting %d file(s)..." % total))

        def status_callback(item_id, message):
            self._enqueue_ui(lambda current_id=item_id: self._update_item_status(current_id, message))

        def convert_item(item):
            if self.closed:
                raise RuntimeError("Cancelled")
            item.outputs = []
            args = build_args_for_settings(item.settings, frequency_text)
            source_log = item.preview_log.copy() if item.preview_log is not None else None
            out_path = build_output_path(item.path, output_dir, item.output_stem)
            written_files = process_log_file(
                item.path,
                item.detected_type,
                out_path,
                args,
                can_db=can_db,
                settings=item.settings,
                status_callback=lambda message: status_callback(item.item_id, message),
                source_data_log=source_log,
                output_stem=item.output_stem,
                channel_unit_chart=channel_unit_chart,
            )
            return item.item_id, written_files

        executor = DaemonThreadPoolExecutor(max_workers=workers, thread_name_prefix="convert")
        self.active_executor = executor
        try:
            future_map = {executor.submit(convert_item, item): item for item in items}
            for future in concurrent.futures.as_completed(future_map):
                if self.closed:
                    break
                item = future_map[future]
                completed += 1
                try:
                    item_id, written_files = future.result()
                except Exception as exc:
                    if str(exc) != "Cancelled":
                        self._enqueue_ui(lambda current_id=item.item_id, error=str(exc): self._mark_item_error(current_id, error))
                else:
                    self._enqueue_ui(lambda current_id=item_id, outputs=written_files: self._mark_item_complete(current_id, outputs))
                progress = (completed / total) * 100
                self._enqueue_ui(
                    lambda count=completed, prog=progress, total_count=total: self._update_generation_progress(count, total_count, prog),
                )
        finally:
            try:
                executor.shutdown(wait=not self.closed, cancel_futures=self.closed)
            except Exception:
                pass
            self.active_executor = None

        if not self.closed:
            self._enqueue_ui(self._finish_generation)

    def _update_item_status(self, item_id, status_text):
        item = self.file_items.get(item_id)
        if item is None:
            return
        item.status = status_text
        self._refresh_tree_row(item_id)
        self.summary_var.set("%s: %s" % (item.name, status_text))

    def _mark_item_complete(self, item_id, outputs):
        item = self.file_items.get(item_id)
        if item is None:
            return
        item.outputs = outputs
        item.status = "Done"
        item.detail = ", ".join(outputs)
        self._refresh_tree_row(item_id)

    def _mark_item_error(self, item_id, error_message):
        item = self.file_items.get(item_id)
        if item is None:
            return
        item.status = "Error"
        item.detail = error_message
        self._refresh_tree_row(item_id)
        self.summary_var.set("%s failed: %s" % (item.name, error_message))

    def _update_generation_progress(self, completed, total, progress):
        self.progressbar["value"] = progress
        self.status_var.set("Converted %d of %d file(s)" % (completed, total))

    def _finish_generation_with_error(self, error_message):
        self._set_running_state(False)
        self.status_var.set("Generation stopped")
        self.summary_var.set(error_message)
        messagebox.showerror("Generation Error", error_message)

    def _finish_generation(self):
        self._set_running_state(False)
        self.progressbar["value"] = 100
        done_count = sum(1 for item in self.file_items.values() if item.status == "Done")
        error_count = sum(1 for item in self.file_items.values() if item.status == "Error")
        self.status_var.set("Finished")
        self.summary_var.set(
            "Completed %d file(s) with %d error(s)." % (done_count, error_count)
        )



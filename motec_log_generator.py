    def _add_input_path(self, path):
        if not path:
            return
        expanded_path = os.path.expanduser(path)
        if os.path.isdir(expanded_path):
            for file_path in flatten_input_paths([expanded_path], self.input_filter_var.get()):
                self._add_input_path(file_path)
            return

        if not os.path.isfile(expanded_path):
            return

        detected_type = detect_log_type(expanded_path)
        if detected_type is None:
            return

        if self.input_filter_var.get() != AUTO_DETECT and detected_type != self.input_filter_var.get():
            return

        if any(
            item.path == expanded_path and item.derived_from is None
            for item in self.file_items.values()
        ):
            return

        item = FileItem(
            item_id=self._new_item_id(),
            path=expanded_path,
            detected_type=detected_type,
        )
        self.file_items[item.item_id] = item
        self.files_tree.insert(
            "",
                tk.END,
                iid=item.item_id,
                text=item.name,
                values=(item.detected_type, item.duration_text, item.size_text),
            )
        self._refresh_tree_row(item.item_id)
        self._update_file_column_width()

        if not self.output_dir.get():
            self.output_dir.set(os.path.dirname(expanded_path))

        self._load_preview_for_item(item, show_placeholder=False)


    def _loaded_preview_bytes(self, items=None):
        total_bytes = 0
        for item in items or self.file_items.values():
            if item.preview_log is None:
                continue
            try:
                total_bytes += os.path.getsize(item.path)
            except OSError:
                continue
        return total_bytes

    def _capture_queue_snapshot(self):
        keep_loaded_previews = self._loaded_preview_bytes() <= UNDO_PREVIEW_LIMIT_BYTES
        ordered_items = []
        for item_id in self.files_tree.get_children():
            if item_id not in self.file_items:
                continue
            source_item = self.file_items[item_id]
            copied_item = copy.copy(source_item)
            copied_item.settings = source_item.settings.copy()
            copied_item.outputs = list(source_item.outputs)
            copied_item.preview_channels = list(source_item.preview_channels)
            copied_item.preview_loading = False
            copied_item.preview_token = 0
            if not keep_loaded_previews:
                copied_item.preview_log = None
                copied_item.preview_channels = []
            if copied_item.preview_log is None and not copied_item.preview_error and copied_item.status != "Done":
                copied_item.status = "Queued"
                copied_item.detail = ""
            ordered_items.append(copied_item)
        return {
            "items": ordered_items,
            "selected_ids": list(self.files_tree.selection()),
            "focus_id": self.files_tree.focus(),
            "next_item_number": self.next_item_number,
            "keep_loaded_previews": keep_loaded_previews,
        }

    def _update_undo_button_state(self):
        if not hasattr(self, "undo_btn"):
            return
        if self.generation_running or not self.undo_stack:
            self.undo_btn.config(state=tk.DISABLED)
        else:
            self.undo_btn.config(state=tk.NORMAL)

    def _push_undo_state(self):
        self.undo_stack.append(self._capture_queue_snapshot())
        if len(self.undo_stack) > self.max_undo_steps:
            self.undo_stack = self.undo_stack[-self.max_undo_steps :]
        self._update_undo_button_state()

    def _restore_queue_snapshot(self, snapshot):
        for item_id in list(self.files_tree.get_children()):
            self.files_tree.delete(item_id)

        self.file_items.clear()
        self.next_item_number = snapshot["next_item_number"]
        self.preview_request_id = None
        self.current_preview_id = None
        self.current_plot_selection = None

        for item in snapshot["items"]:
            self.file_items[item.item_id] = item
            self.files_tree.insert(
                "",
                tk.END,
                iid=item.item_id,
                text=item.name,
                values=(item.detected_type, item.duration_text, item.size_text),
            )
            self._refresh_tree_row(item.item_id)

        selected_ids = [item_id for item_id in snapshot["selected_ids"] if item_id in self.file_items]
        if selected_ids:
            self.files_tree.selection_set(selected_ids)
        focus_id = snapshot["focus_id"]
        if focus_id in self.file_items:
            self.files_tree.focus(focus_id)
            self.files_tree.see(focus_id)

        self._update_file_column_width()
        self._update_undo_button_state()
        self._on_tree_selection_changed()

    def _on_undo(self, event=None):
        focus_widget = self.root.focus_get()
        if isinstance(focus_widget, (tk.Entry, ttk.Entry, ttk.Combobox, tk.Text)):
            return
        if not self.undo_stack:
            return "break"
        snapshot = self.undo_stack.pop()
        self._restore_queue_snapshot(snapshot)
        self.summary_var.set("Undid the last queue change.")
        return "break"

    def _update_file_column_width(self):
        longest_text = "File"

def run_cli(args):
    if args.log:
        args.log = os.path.expanduser(args.log)
    if args.dbc:
        args.dbc = os.path.expanduser(args.dbc)
    if args.unit_chart:
        args.unit_chart = os.path.expanduser(args.unit_chart)
    if args.output:
        args.output = os.path.expanduser(args.output)

    try:
        resolve_frequency(args.frequency)
    except ValueError as exc:
        print("ERROR: %s" % exc)
        return 1

    if not os.path.exists(args.log):
        print("ERROR: log file or directory %s does not exist" % args.log)
        return 1

    if args.log_type == "CAN" and not args.dbc:
        print("ERROR: --dbc is required when log type is CAN")
        return 1

    try:
        channel_unit_chart = load_channel_unit_chart(args.unit_chart)
    except Exception as exc:
        print("ERROR: %s" % exc)
        return 1

    can_db = None
    if args.log_type == "CAN":
        try:
            can_db = load_can_database(args.dbc)
        except Exception as exc:
            print("ERROR: %s" % exc)
            return 1

    exit_code = 0
    if os.path.isdir(args.log):
        input_dir = args.log
        output_dir = args.output if args.output else input_dir

        if output_dir and not os.path.isdir(output_dir):
            os.makedirs(output_dir, exist_ok=True)

        for file_path in flatten_input_paths([input_dir], args.log_type):
            try:
                process_log_file(
                    file_path,
                    args.log_type,
                    build_output_path(file_path, output_dir),
                    args,
                    can_db=can_db,
                    channel_unit_chart=channel_unit_chart,
                )
            except Exception as exc:
                print("ERROR processing %s: %s" % (file_path, exc))
                exit_code = 1
    else:
        try:
            process_log_file(
                args.log,
                args.log_type,
                args.output,
                args,
                can_db=can_db,
                channel_unit_chart=channel_unit_chart,
            )
        except Exception as exc:
            print("ERROR: %s" % exc)
            exit_code = 1

    return exit_code


def create_root():
    if TkinterDnD is not None:
        try:
            return TkinterDnD.Tk()
        except Exception:
            pass
    return tk.Tk()


def main():
    if len(sys.argv) == 1:
        root = create_root()
        MotecLogGUI(root)
        root.mainloop()
        return 0

    parser = build_argument_parser()
    args = parser.parse_args()
    return run_cli(args)


if __name__ == "__main__":
    sys.exit(main())

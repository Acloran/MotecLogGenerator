    def _on_tree_selection_changed(self, _event=None):
        selected_items = self._selected_items()
        self.metadata_dirty_fields.clear()
        self._cancel_segment_cell_edit()
        if len(selected_items) != 1 or selected_items[0].item_id != self.current_preview_id:
            self.current_plot_selection = None
            self._clear_pending_segment()

        if not selected_items:
            self.preview_title_var.set("Select a file to preview")
            self.preview_subtitle_var.set("The graph will default to the best speed channel it can find.")
            self.metadata_info_var.set("Select one or more files to edit their MoTeC metadata.")
            self.selection_info_var.set("Use Split for automatic cuts, then select a row and drag on the graph to edit it.")
            self._populate_metadata_editor([])
            self._show_plot_placeholder("Select one file to preview.")
            self._set_editor_enabled(False)
            return

        self._populate_metadata_editor(selected_items)

        if len(selected_items) > 1:
            self.preview_title_var.set("%d files selected" % len(selected_items))
            self.preview_subtitle_var.set("Preview is hidden while multiple files are selected. Metadata edits apply to every selected file.")
            self.metadata_info_var.set("Blank fields can mean mixed values. Only fields you edit will be applied.")
            self.selection_info_var.set("Preview is hidden for multi-select so you can batch-edit metadata safely.")
            self._show_plot_placeholder(
                "%d files selected.\nPreview is hidden while you batch-edit metadata."
                % len(selected_items)
            )
            self._set_editor_enabled(True, single_preview=False)
            return

        item = selected_items[0]
        self.preview_title_var.set(item.name)
        self.preview_subtitle_var.set(item.path)
        self.metadata_info_var.set("Changes here affect only the selected file until you select more than one.")
        self._set_editor_enabled(True, single_preview=True)
        self._load_preview_for_item(item, show_placeholder=True)

